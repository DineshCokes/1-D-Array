import java.util.Arrays;

public class Sort {
    public static void main(String[] args) {
        int a[]={1,4,3,1,7,10,5};
        char b[]={'a','c','e','b'};
        Arrays.sort(a);
        Arrays.sort(b);
        System.out.println();
        System.out.println(Arrays.toString(a));
        System.out.println(Arrays.toString(b));
    }
    
}
